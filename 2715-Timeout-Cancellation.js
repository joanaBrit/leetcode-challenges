/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function (fn, args, t) {
    const timer = setTimeout(() => {
        fn(...args)
    }, t)

    return function cancelFn(){
        clearTimeout(timer)
    }
};

/**

Function fn should be delayed by t milliseconds before being executed.
Schedule the execution of fn using setTimeout.
Use the spread operator (...args) to pass the arguments to fn when it is invoked.
Return a cancel function (cancelFn) that allows clearing the scheduled execution.
Inside the cancel function, use clearTimeout to stop the execution of fn.

 */

